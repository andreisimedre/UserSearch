import UIKit

extension UIColor {
    static let fallBackColor = UIColor.white
    
    static let silver = UIColor(named: "silver") ?? fallBackColor
    static let skyblueDark = UIColor(named: "skyblueDark") ?? fallBackColor
    static let periwinkle = UIColor(named: "periwinkle") ?? fallBackColor
    static let rose = UIColor(named: "rose") ?? fallBackColor
    static let skyblue = UIColor(named: "skyblue") ?? fallBackColor
}
